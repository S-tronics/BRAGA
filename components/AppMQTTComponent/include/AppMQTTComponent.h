/**
 * @brief   Initialise function for this module
 *
 *
 */
void AppMQT_client_Init();

/**
 * @brief Function that wraps around the MQTT esp client library
 *
 * @param topic Topic to publish to
 * @param data Data to publish
 * @param qos QoS level => 0 (Fire and forget), 1 (At least once) or 2 (Exactly once)
 * @param retain If true, the last topic message will be stored in broker
 */
void AppMQTT_client_publish(char *topic, char *data, int qos, int retain);

/**
 * @brief Function that wraps around the MQTT esp client library
 *
 * @param topic Topic to subscribe to
 * @param qos QoS level => 0 (Fire and forget), 1 (At least once) or 2 (Exactly once)
 */
void AppMqtt_client_subscribe(char *topic, int qos);

/**
 * @brief Function to publish data to Diagnostics topic. This is used for example in logging
 *        and keep-alive messages.
 *
 * @param data data to publish
 * @param qos QoS level => 0 (Fire and forget), 1 (At least once) or 2 (Exactly once)
 * @param retain If true, the last topic message will be stored in broker
 */
void AppMQTT_client_publish_diagnostics(char *data, int qos, int retain);
/**
 * @brief   Initialise function for this module
 *
 *
 */
void AppMQTT_client_publish_setup(char *data, int qos, int retain);

